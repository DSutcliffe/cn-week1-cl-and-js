// Assignment Operators
let numberOne = 10
let numberTwo = 5

numberOne += numberTwo
console.log(numberOne)

numberOne -= numberTwo
console.log(numberOne)

numberOne *= numberTwo
console.log(numberOne)

numberOne /= numberTwo
console.log(numberOne)

numberOne ++
console.log(numberOne)

numberOne --
console.log(numberOne)