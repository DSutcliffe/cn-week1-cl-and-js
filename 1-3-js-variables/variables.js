// Variables
let firstName = "Danny"
let lastName = "Sutcliffe"
let age = 41
console.log(firstName)
console.log("Hello, " + firstName + ". How are you?")
console.log(`Hello, ${firstName}. How are you?`)                // back ticks `` allows use of ${...} to call variable within string
console.log(`Hello, ${firstName} ${lastName}. How are you?`)
console.log(`Hello, ${firstName} ${lastName}. Oh, you are ${age} years old`)
